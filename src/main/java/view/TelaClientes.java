    public void listarClientes() {
    List<model.Cliente> lista = new dao.ClienteDAO().read();
    DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();
    modelo.setRowCount(0); // Limpa a tabela

    for (Cliente c : lista) {
        modelo.addRow(new Object[]{
            c.getId(), 
            c.getNome(), 
            c.getCpf(),
            c.getTelefone(), 
            c.getEmail()
        });
    }
}

